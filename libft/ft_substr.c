#include "libft.h"

char	*start_len(void)
{
	char *str;

	if (!(str = malloc(1 * sizeof(char))))
		return (NULL);
	*str = '\0';
	return (str);
}

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	size_t	j;
	char	*str;

	i = 0;
	j = 0;
	if (!s)
		return (NULL);
	while (i < start)
		i++;
	if (start > len)
		return (str = start_len());
	if (!(str = malloc((len + 1) * sizeof(char))))
		return (NULL);
	while (j < len)
	{
		str[j] = s[i];
		j++;
		i++;
	}
	str[j] = '\0';
	return (str);
}
